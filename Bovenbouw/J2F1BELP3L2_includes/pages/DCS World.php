  <h1>DCS World</h1>

	<P>Digital Combat Simulator World is een hyperrealistische militair-simulatorspel, gemaakt door Eagle Dynamics, waarin spelers in gevechtsvliegtuigen en helikopters vliegen. De game staat bekend om zijn aandacht voor detail, met nauwkeurige simulaties van verschillende militaire vliegtuigen, wapensystemen en gevechtstactieken. DCS World is gratis te spelen, maar biedt een breed scala aan extra vliegtuigen en modules die je kunt kopen om je ervaring uit te breiden.</P>

	<P>In DCS World heb je de mogelijkheid om een breed aantal aan vliegtuigen te besturen, zoals de F-16 Fighting Falcon, de A-10C Warthog, en de F/A-18 Hornet. Elk vliegtuig is tot in de kleinste details nagebootst, van de cockpitinstrumenten tot de vliegeigenschappen, en vereist uitgebreide training om volledig onder de knie te krijgen. Voor nieuwe spelers kan dit een flinke leercurve zijn.</P>

